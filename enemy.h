#define	ENEMY_MAX (7)

/*******************************************************************************
* 構造体定義
*******************************************************************************/
struct ENEMY {
	
	// 座標
	int x;
	int y;
	int old_x;
	int old_y;

	// status
	int hp, hp_max;
	int atk, atk_max;
	int mp, mp_max;
	int speed, speed_max;
	int lucky, lucky_max;
	int critical, critical_max;

	// when player hit on enemy flag
	bool collision;

};

